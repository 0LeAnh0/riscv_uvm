/////////////////////////////////////////PC_MODULE///////////////////////////////////////////////////////////////
module PC
(
    input clk,res,
    input [31:0] in,
    output reg [31:0] out
);
 
  always@(posedge clk) begin
    if(res)
      out<=32'b0;
    //else if(write)
    else
      out<=in;
  end

endmodule
